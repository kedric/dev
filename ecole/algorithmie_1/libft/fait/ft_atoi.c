#include <stdlib.h>

int ft_atoi(const char *nptr)
{
	int i[3];
	
	i[0] = 0;
	i[1] = 0;
	i[2] = 1;

	while(nptr[i[0]] <= 13 && nptr[i[0]] >= 9 || nptr[i[0]] == '\n' 
		  || nptr[i[0]] == 32 || nptr[i[0]] == 43)
		i[0]++;
	if (nptr[i[0]] == '-')
	{
		i[2] = -1;
		i[0]++;
	}
	while (nptr[i[0]] != '\0')
	{
		if (nptr[i[0]] >= 48 && nptr[i[0]] <= 57)
		{
			i[1] = i[1] * 10 + nptr[i[0]] - '0';
			i[0]++;
		}
		else
			return (i[1] * i[2]);
	}
	return (i[1] * i[2]);
}
