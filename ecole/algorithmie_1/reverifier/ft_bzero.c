#include <strings.h>

void	ft_bzero(void *s, size_t n)
{
	int i;
	char *str;

	i = 0;
	if (s != 0 && n > 0)
	{
		str = (char*)s;
		while(i < n)
		{
			*((char *)s+i) = '\0';
			i++;
		}
	}
}
